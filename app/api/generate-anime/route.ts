function getRandomAnime(inputs: { theme?: string; tone?: string; setting?: string; customRequest?: string }) {
  // Get a random base anime
  const base = FALLBACK_ANIME[Math.floor(Math.random() * FALLBACK_ANIME.length)]
  
  // Add some variation based on inputs if provided
  const title = inputs.theme 
    ? `${base.title.split(':')[0]}: ${inputs.theme.toUpperCase()}`
    : base.title
    
  return {
    ...base,
    title,
  }
}

export async function POST(req: Request) {
  const { theme, tone, setting, customRequest } = await req.json()
  
  // Use fallback anime generation (no AI dependency)
  const anime = getRandomAnime({ theme, tone, setting, customRequest })
  
  // Simulate a brief generation delay for UX
  await new Promise(resolve => setTimeout(resolve, 1500))
  
  return Response.json({ anime })
}
